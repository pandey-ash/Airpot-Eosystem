    private void populateTable(List<Airplane> airplanes) {
        DefaultTableModel model = (DefaultTableModel) tblResult.getModel();
        model.setRowCount(0);
        String date = df.format(txtDate.getDate());

        for(Airplane airplane : airplanes)
        {
            Object[] row = new Object[7];
            row[0] = airplane.getAirline();
            row[1] = airplane;
            row[2] = airplane.getDepartureTime();
            row[3] = airplane.getPrice();
            row[4] = airplane.getTotalHours();
            row[5] = Constant.noOfSeats;
            if(airplane.getBooking().get(date) != null)
            {
                row[5] = Constant.noOfSeats - airplane.getBooking().get(date).size();
            }
            model.addRow(row);
        }
    }
