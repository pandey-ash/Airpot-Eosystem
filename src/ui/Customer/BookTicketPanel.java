    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String departureAirport = (String) comboBoxDeparture.getSelectedItem();
        String arrivalAirport = (String) comboBoxArrival.getSelectedItem();
        
        String date = df.format(txtDate.getDate());
//        Integer noOfTraveller = Integer.valueOf((String) comboBoxNoOfTraveller.getSelectedItem());
        if(departureAirport.equals(arrivalAirport))
        {
            showMessageDialog(this, "Departure and Arrival airport cannot be same", "Error", ERROR_MESSAGE);
            return;
        }
        
        
        Airport departure = ecoSystem.getAirportDirectory().getAirportFromName(departureAirport);
        Airport arrival = ecoSystem.getAirportDirectory().getAirportFromName(arrivalAirport);
        List<Airplane> result = new ArrayList();
        for(Airplane airplane : ecoSystem.getAirplaneDirectory().getAirplanes())
        {
            if(airplane.getDepartureAirport().equals(departure) 
                    && airplane.getArrivalAirport().equals(arrival) 
                    && (airplane.getBooking().get(date) == null || airplane.getBooking().get(date).size() < Constant.noOfSeats  ))
            {
                result.add(airplane);
            }
        }
        populateTable(result);
    }//GEN-LAST:event_jButton1ActionPerformed

